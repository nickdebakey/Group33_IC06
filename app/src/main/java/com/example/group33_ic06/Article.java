public class Article {
    String title;
    String date;
    String iv_url;
    String description;

    public Article() {
    }

    public Article(String title, String date, String iv_url, String description) {
        this.title = title;
        this.date = date;
        this.iv_url = iv_url;
        this.description = description;
    }
}
